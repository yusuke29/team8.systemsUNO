#四則演算

#変数
a=20
b=10
c=3

print(f"a=20")
print(f"b=10")
print(f"c=3")

#足し算
num1=a+b
print(f"num1=a+b 答えは{num1}")
#引き算
num2=a-b
print(f"num2=a-b 答えは{num2}")
#掛け算
num3=a*b
print(f"num3=a*b 答えは{num3}")
#割り算
num4=a/b
print(f"num4=a/b 答えは{num4}")
#余り算
num5=a%c
print(f"num5=a%c 答えは{num5}")
#乗算
num6=a**c
print(f"num6=a**c 答えは{num6}")
